    private void btnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgualActionPerformed
        // TODO add your handling code here:
          Toolkit.getDefaultToolkit().beep();
        if(evt !=null){
         b = Float.parseFloat(txtPantalla.getText());
        }
         switch(operando){
             case '+':
                 txtPantalla.setText(a+b+"");
                 frmHistorial.jtfHistorial.append("Suma: "+a+" + "+b+" = "+ txtPantalla.getText()+"\n");
             break;
              case '-': 
                   txtPantalla.setText(a-b+"");
                   frmHistorial.jtfHistorial.append("Resta: "+a+" - "+b+" = "+ txtPantalla.getText()+"\n");
             break;
              case '*': 
                   txtPantalla.setText(a*b+"");
                   frmHistorial.jtfHistorial.append("Multiplicacion: "+a+" * "+b+" = "+ txtPantalla.getText()+"\n");
             break;
              case '/':     
                   txtPantalla.setText(a/b+"");
                   frmHistorial.jtfHistorial.append("Division: "+a+" / "+b+" = "+ txtPantalla.getText()+"\n");
             break;
              default: 
                  JOptionPane.showMessageDialog(this, "Debe presionar una operacion, +, -, *, /");
         }
         operando = ' ';
    }//GEN-LAST:event_btnIgualActionPerformed

