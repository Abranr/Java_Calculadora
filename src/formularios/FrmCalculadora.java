    private void txtPantallaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPantallaKeyReleased
        // TODO add your handling code here:
        System.out.print("tecla presionada: "+evt.getKeyChar());
        switch (evt.getKeyChar()){
            case '0':
                btnCero.doClick();
                break;
            case '1':
                btnUno.doClick();
                break;
                  case '2':
                      btnDos.doClick();
                break;
            case '3':
                btnTres.doClick();
                break;
                  case '4':
                      btnCuatro.doClick();
                break;
            case '5':
                btnCinco.doClick();
                break;
                  case '6':
                      btnSeis.doClick();
                break;
            case '7':
                btnSiete.doClick();
                break;
                  case '8':
                      btnOcho.doClick();
                break;
            case '9':
                btnNueve.doClick();
                break;
                   case '+':
                btnSuma.doClick();
                break;
                  case '-':
                      btnResta.doClick();
                break;
            case '*':
                btnMultiplicacion.doClick();
                break;
                  case '/':
                      btnDivision.doClick();
                break;
            case '.':
                btnPuntoComa.doClick();
                break;
                 case ',':
                btnPuntoComa.doClick();
                break;
                  case 'c':
                      btnLimpiarTodo.doClick();
                break;
            case '%':
                btnPorciento.doClick();
                break;
                       case '=':
                      btnIgual.doClick();
                break;
                     case 'd':
                      btnEliminarDigito.doClick();
                break;
                
                
        
        }
    }//GEN-LAST:event_txtPantallaKeyReleased

